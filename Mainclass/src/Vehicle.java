public class Vehicle {
    @Override
    public String toString() {
        return "Vehicle{" +
                "noOfDoors=" + noOfDoors +
                ", noOfTyres=" + noOfTyres +
                ", noOfMirrors=" + noOfMirrors +
                ", noOfMileage=" + noOfMileage +
                '}';
    }

    public int noOfDoors;
    public int noOfTyres;
    public int noOfMirrors;
    public int noOfMileage;
}
